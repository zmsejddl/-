package section02;

public class EX02_17 {
	public static void main(String[] args) {
		int alphabetA = 'A'; // 'A'와 매핑된 유니코드(정수) 65가 저장됨
		int alphabetB = 'B'; // 'B'와 매핑된 유니코드(정수) 66이 저장됨
		System.out.println(alphabetA);
		System.out.println(alphabetB);

		char alphabetC = 67; // 유니코드(정수) 67과 매핑된 'C'가 저장됨
		System.out.println(alphabetC);
	}
}
